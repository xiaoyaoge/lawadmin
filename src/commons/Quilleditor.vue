import { quillEditor } from 'vue-quill-editor'
export default {
    props: {
        /*编辑器的内容*/
        value: {
            type: String
        },
        /*上传图片的地址*/
        uploadUrl: {
            type: String
        },
        /*上传图片的file控件name*/
        fileName: {
            type: String,
        },
        /*图片大小*/
        maxSize: {
            type: Number,
            default: 1024 * 5 //kb
        },
        /*使用使用裁切*/
        canCrop: {
            type: Boolean,
            default: false
        },
        /*裁切的最小尺寸*/
        width: {
            type: Number,
            default: 200
        },
        /*裁切的最小尺寸*/
        height: {
            type: Number,
            default: 200
        }
    },
    data() {
        return {
            uploadPolicy: {
                host: ''
            },
            uploadConfig: {
                multiple: true,
                thread: 5,
                data: {},
                accept: 'image/png,image/gif,image/jpeg,image/webp'
            },
            fileList: [],
            uploadUrls: '',
            content: '',
            editorOption: {
                modules: {
                    toolbar: '#toolbar',
                }
            },
            /*显示裁切控件*/
            showCrop: false
        }
    },
    methods: {
        onChange() {
            this.$emit('input', this.content)
        },
        /*选择上传图片切换*/
        onFileChange(e) {
            var fileInput = e.target;
            if (fileInput.files.length == 0) {
                return;
            }
            if (fileInput.files[0].size > 1024 * 1024 * this.maxSize) {
                alert('图片过大');
                return;
            }
            if (!this.uploadUrl) {
                console.log('no editor uploadUrl');
                return;
            }
            var self = this;
            var data = new FormData;
            data.append(this.fileName, fileInput.files[0], name);
            this.editor.focus();
            var xhr = new XMLHttpRequest();
            xhr.open('post', this.uploadUrl);
            xhr.responseType = 'json';
            xhr.send(data);
            xhr.onload = function() {
                if (xhr.status == 200) {
                    self.editor.insertEmbed(self.editor.getSelection().index, 'image', xhr.response.url);
                }
            }
        },
        /*裁切上传成功 res根据上传接口值获取*/
        onUploadSuccess: function(res) {
            this.editor.focus();
            this.editor.insertEmbed(this.editor.getSelection().index, 'image', res.url);
        },
        /*点击上传图片按钮*/
        imgClick() {
            if (this.canCrop) {
                this.showCrop = true;
            } else {
                this.$http.post('upload/getAliyunPostPolicy', {}).then((res) => {
                    this.$http.aop(res, () => {
                        this.uploadPolicy = res.body.data || {};
                        this.uploadConfig.data.policy = this.uploadPolicy.policy;
                        this.uploadConfig.data.OSSAccessKeyId = this.uploadPolicy.accessid;
                        this.uploadConfig.data.signature = this.uploadPolicy.signature;

                    });
                }, (res) => {
                    this.$http.aop(res);
                });

            }
        },
        uploadSuccess(response, file, fileList) { //文件长传成功  
            console.log(response);
            let url = this.uploadPolicy.host + '/' + this.uploadConfig.data.key;
            //this.form[this.uploadType] = url;
            this.onUploadSuccess({ url: url })
            //this.editor.insertEmbed(this.editor.getSelection().index, 'image', url);
        },
        beforeUpload(file) {
            let key = this.uploadPolicy.dir + '/' + md5('' + Date.now + this.uploadPolicy.mid + Math.random());
            this.uploadConfig.data.key = key;
            const isLt5M = file.size / 1024 / 1024 < 5;
            if (!isLt5M) {
                this.fileList = [];
                this.$message.error('上传图片大小不能超过 5MB!');
            }
            return isLt5M;
        },
        uploadRemove(file, fileList) {
            if (fileList.length > 0) { this.fileList = fileList.slice(-1); }
        },
        uploadChange(file, fileList) {
            if (fileList.length > 0) {
                this.fileList = fileList.slice(-1);
            } else {
                this.fileList = [];
            }

        },
        uploadPictureCardPreview(file) {
            this.dialogImageUrl = file.url;
            this.dialogVisible = true;
        },
        uploadError(file, fileList) {
            this.$message({
                type: 'info',
                message: '文件上传失败'
            });
        }
    },
    computed: {
        editor() {
            return this.$refs.myTextEditor.quill;
        }
    },
    components: {
        quillEditor
    },
    mounted() {
        this.content = this.value;
        this.imgClick();
    },
    watch: {
        'value' (newVal, oldVal) {
            if (this.editor) {
                if (newVal !== this.content) {
                    this.content = newVal
                }
            }
        },
    }
}
